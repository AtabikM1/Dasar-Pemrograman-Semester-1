
public class ContohTipeData06 {
    public static void main(String[] args) {
    char GolonganDarah = 'A';
    byte jarak = (byte) 130;
    short JumlahPendudukDalamSatuDusun = 1025;
    float suhu = 60.50F;
    double berat = 0.54678123;
    long saldo = 190000000;
    int angkadesimal = 0x54;

    System.out.println("Golongan darah               :"+ (byte)GolonganDarah);
    System.out.println("jarak                        :"+ jarak);
    System.out.println("Jumlah penduduk satu dusun   :"+ JumlahPendudukDalamSatuDusun);
    System.out.println("suhu                         :"+ suhu);
    System.out.println("berat                        :"+ (float)berat);
    System.out.println("saldo                        :"+ saldo);
    System.out.println("angkadesimal                 :"+ angkadesimal);
    }
}
