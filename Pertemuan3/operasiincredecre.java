package Pertemuan3;
public class operasiincredecre {
    public static void main(String[] args) {
        int i = 1;
    //increment
        System.out.println("i :" + i);
        System.out.println("++i :" + (5 * ++i));
        System.out.println("i++ :" + (5 * i++));
    //decrement
        System.out.println("--i :" + --i);
        System.out.println("i-- :" + i--);
        System.out.println("i :" + i);
    }
}
